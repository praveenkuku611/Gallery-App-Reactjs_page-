class Gallery extends Component {
  state = {
    activeImage: imagesList[0].imageUrl,
    imageText: imagesList[0].imageAltText,
  }

  getFilteredImage = (imageUrl, imageAltText) => {
    this.setState({activeImage: imageUrl, imageText: imageAltText})
  }

  render() {
    const {activeImage, imageText} = this.state
    return (
      <div className="bg-container">
        <div className="container">
          <img src={activeImage} alt={imageText} />
          <div className="items-container">
            <h1 className="heading">Nature Photography</h1>
            <div className="list-image-container">
              <p className="para">Nature Photography by Rahul</p>
            </div>
            <ul className="image-container">
              {imagesList.map(eachImage => (
                <ThumbnailItem
                  imageDetails={eachImage}
                  getFilteredImage={this.getFilteredImage}
                  key={eachImage.id}
                  isActive={eachImage.imageUrl === activeImage}
                />
              ))}
            </ul>
          </div>
        </div>
      </div>
    )
  }
}

